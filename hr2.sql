SELECT (4*PRICE*0.05) INTO RESTOCKING_FEE
FROM INVENTORY WHERE PRODUCT_ID = 2;

UPDATE INVENTORY 
SET STOCK = STOCK + (4-(4*0.05))
WHERE PRODUCT_ID = 2;

END;
COMMIT;

EXCEPTION
WHEN OTHERS ROLLBACK TO STOCK_UPDATE;
