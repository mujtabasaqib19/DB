    TRANSACTION_ID NUMBER PRIMARY KEY,
    ACCOUNT_ID NUMBER,
    TYPE VARCHAR2(20),
    AMOUNT NUMBER,
    TRANSACTION_DATE DATE,
    FOREIGN KEY (ACCOUNT_ID) REFERENCES BANK_ACCOUNTS(ACCOUNT_ID)
